bool visited[100];
int in_time[100];
int low[100];
int timer;
void dfs(int s, int par)
{
	// cout << s << " ->";
	visited[s] = true;
	in_time[s] = low[s] = timer;
	timer++;
	for (int child : adj[s])
	{
		if (child == par) continue;

		else if (visited[child] == true)
		{
			//back edge is found
			low[s] = min(low[s] , in_time[child]);
		}
		else
		{
			dfs(child, s);
			//just a forward edge
			if (low[child] > in_time[s])
				cout << s << "->" << child << " bridge" << endl;

			low[s] = min(low[child] , low[s]);
		}
	}
}
void addEdge(int x , int y)
{
	//Undirected Graph
	adj[x].push_back(y);                   //Edge from vertex x to vertex y
	adj[y].push_back(x);				  //Edge from vertex y to vertex x
}

